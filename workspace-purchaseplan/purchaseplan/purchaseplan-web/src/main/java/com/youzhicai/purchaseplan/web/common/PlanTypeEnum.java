package com.youzhicai.purchaseplan.web.common;

public enum PlanTypeEnum {
    
    MONTHLYPLAN(1, "月度计划"), EMERGENCYPLAN(2, "紧急计划");

    private int status;
    private String value;

    private PlanTypeEnum(int status, String value) {
        this.status = status;
        this.value = value;
    }

    public int getStatus() {
        return status;
    }

    public String getValue() {
        return value;
    }
}
