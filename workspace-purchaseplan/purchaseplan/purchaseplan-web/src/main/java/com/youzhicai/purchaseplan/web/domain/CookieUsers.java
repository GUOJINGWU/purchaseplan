package com.youzhicai.purchaseplan.web.domain;

public class CookieUsers {
    private String CompanyId;
    private String IsShowMsg;
    private String Name;
    private String RandomNum;
    private String SessionId;
    private String SessionKey;
    private String SessionUserName;
    private String Site;
    private String SsoInfo;

    public CookieUsers() {
        super();
    }

    public String getCompanyId() {
        return CompanyId;
    }

    public void setCompanyId(String companyId) {
        CompanyId = companyId;
    }

    public String getIsShowMsg() {
        return IsShowMsg;
    }

    public void setIsShowMsg(String isShowMsg) {
        IsShowMsg = isShowMsg;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getRandomNum() {
        return RandomNum;
    }

    public void setRandomNum(String randomNum) {
        RandomNum = randomNum;
    }

    public String getSessionId() {
        return SessionId;
    }

    public void setSessionId(String sessionId) {
        SessionId = sessionId;
    }

    public String getSessionKey() {
        return SessionKey;
    }

    public void setSessionKey(String sessionKey) {
        SessionKey = sessionKey;
    }

    public String getSessionUserName() {
        return SessionUserName;
    }

    public void setSessionUserName(String sessionUserName) {
        SessionUserName = sessionUserName;
    }

    public String getSite() {
        return Site;
    }

    public void setSite(String site) {
        Site = site;
    }

    public String getSsoInfo() {
        return SsoInfo;
    }

    public void setSsoInfo(String ssoInfo) {
        SsoInfo = ssoInfo;
    }

}
