package com.youzhicai.purchaseplan.web.util;

public class IntegerUtil {
    /**
     * 判断Integer 与 int 数值相等（null判断）
     * 
     * @param param1--'Integer类型数据'
     * @param param2--'int数值'
     * @return
     */
    public static Boolean IsIntegerEqualInt(Integer param1, int param2) {
        if (param1 == null) {
            return Boolean.FALSE;
        }
        if (param1 == param2) {
            return Boolean.TRUE;
        }
        return Boolean.FALSE;
    }

    /**
     * 判断Integer 与 int 数值不相等（null判断）
     * 
     * @param param1--'Integer类型数据'
     * @param param2--'int数值'
     */
    public static Boolean IsIntegerNotEqualInt(Integer param1, int param2) {
        if (param1 == null) {
            return Boolean.FALSE;
        }
        if (param1 != param2) {
            return Boolean.TRUE;
        }
        return Boolean.FALSE;
    }

    /**
     * 判断Integer 是否为null
     * 
     * @Title: isNullOrEmpty
     * @param param1
     * @return
     * @return: Boolean
     */
    public static Boolean isNullOrEmpty(Integer param1) {
        if (param1 != null) {
            return Boolean.FALSE;
        }
        return Boolean.TRUE;
    }
}
